package babysteps;

public class babysteps_variables {

	String stringy = "Yeller!";
	char kar = 'd';
	byte bit = 1;
	boolean bully = true;
	int inty = 2021;
	long longy = 20212022;
	float floaty = 2021.05f;
	double doubly = 2021.05;
	
	public void lemmePrint() {
		System.out.println("String variable is: " + stringy);
		System.out.println("Char variable is: " + kar);
		System.out.println("Byte variable is: " + bit);
		System.out.println("Boolean variable is: " + bully);
		System.out.println("Integer variable is: " + inty);
		System.out.println("Long variable is: " + longy);
		System.out.println("Float variable is: " + floaty);
		System.out.println("Double variable is: " + doubly);

	}
	
	//Automatic casting is done when widening is wanted.
	//Manual casting is done for when we wanna both narrow and widen.
	
}
